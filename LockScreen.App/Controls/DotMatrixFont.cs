    public const int GlyphWidth = 5;
    public const int GlyphHeight = 7;

    public static bool IsPixelOn(char character, int x, int y)
    {
        if (x < 0 || x >= GlyphWidth || y < 0 || y >= GlyphHeight)
        {
            return false;
        }

        var normalized = char.ToUpperInvariant(character);
        if (!Glyphs.TryGetValue(normalized, out var rows))
        {
            rows = Glyphs[' '];
        }

        return rows[y][x] == '1';
    }

    private static string[] Rows(params string[] rows) => rows;

    private static string[] Empty() => Rows("00000", "00000", "00000", "00000", "00000", "00000", "00000");
